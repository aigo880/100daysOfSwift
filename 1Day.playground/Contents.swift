import UIKit

// VARIABLES

// strings
var str = "Hello, Muggle"
str = "Buy, Muggle"

// integers
var age = 28
var population = 8_000_000

// multi-line strings
var str2 = """
    Hello
    Howdy Ho
    Hi
"""
var str3 = """
    Hello \
    Howdy Ho \
    Hi
"""

// doubles and booleans
var pi = 3.141
var isRight = true

// string interpolation
var userAge = 39
var str4 = "Hello User, Your age \(userAge)"

// constants
let taylor = "swift"

// type annotations
let album: String = "Reputation"
let year: Int = 1989
let height: Double = 1.78
let taylorRocks: Bool = true

// simple types: summary

// 1. You make variables using var and constants using let.
//    It’s preferable to use constants as often as possible.
//
// 2. Strings start and end with double quotes, but if you
//    want them to run across multiple lines you should use three sets of double quotes.
//
// 3. Integers hold whole numbers, doubles hold fractional numbers,
//    and booleans hold true or false.
//
// 4. String interpolation allows you to create strings from other variables and constants,
//    placing their values inside your string.
//
// 5. Swift uses type inference to assign each variable or constant a type,
//    but you can provide explicit types if you want.


