import UIKit

// For loops
let count = 1...10
for number in count {
    print("number - \(number)")
}


let albums = ["Red", "1989", "Reputation"]
for album in albums {
    print(album)
}

for _ in 1...5 {
    print("GO")
}


// While loops
var number = 1

while number <= 20 {
    print(number)
    number += 1
}


// Repeat loops
var number1 = 1

repeat {
    print(number1)
    number1 += 1
} while number1 < 5





